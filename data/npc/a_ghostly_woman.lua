-- A Ghostly Woman (DialogueBuilder)
local npc = DialogueBuilder:new("A Ghostly Woman",
	{lookType = 140, lookHead = 9, lookBody = 85, lookLegs = 10, lookFeet = 85, lookAddons = 1})
npc:greetMessage("I feel you. I hear your thoughts. You are ... alive.")
npc:farewellMessage("Alone ... so alone. So cold.")
npc:walkawayMessage("Alone ... so alone. So cold.")
npc:addResponse("job", "Once I was a member of the order of the nightmare knights. Now I am but a shadow who walks these cold halls.")
npc:addResponse("boots", "The north has a puzzle to complete.")
npc:addVoice("Alone ... so alone. So cold.")
npc:register()
