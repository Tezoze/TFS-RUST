#!/usr/bin/env bash
# Build tibia-ipchanger.exe (MinGW) and refresh servers.txt RSA from repo tibia.pem.
set -euo pipefail

ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
# shellcheck source=lib/reference_paths.sh
. "$ROOT/scripts/lib/reference_paths.sh"
reference_paths_init "$ROOT"
PEM="$(resolve_rsa_pem_path "$ROOT")"

die() { echo "error: $*" >&2; exit 1; }

command -v x86_64-w64-mingw32-g++ >/dev/null \
    || die "install MinGW: sudo pacman -S mingw-w64-gcc"

[[ -f "$PEM" ]] || die "missing RSA key: $PEM"

make -C "$IPCH" all

decimal_modulus() {
    python3 - "$PEM" <<'PY'
import subprocess
import sys
from pathlib import Path

pem = Path(sys.argv[1])
hexmod = subprocess.check_output(
    ["openssl", "rsa", "-in", str(pem), "-modulus", "-noout"], text=True
).strip().split("=", 1)[1]
print(int(hexmod, 16))
PY
}

MOD="$(decimal_modulus)"
HOST="${TIBIA_LOGIN_HOST:-127.0.0.1}"
PORT="${TIBIA_LOGIN_PORT:-7171}"

cat > "$IPCH/servers.txt" <<EOF
# Auto-generated by scripts/build_ipchanger.sh — do not edit RSA by hand.
# VERSION;ALIAS;HOSTNAME;PORT;RSAMODULUS
772;local;${HOST};${PORT};${MOD}
EOF

cat <<EOF
built: $IPCH/build/ipchanger.exe
       $IPCH/build/memscan.exe
config: $IPCH/servers.txt  (772;local;${HOST}:${PORT})

Usage (Tibia.exe must be running under Wine):
  cd $IPCH
  wine build/ipchanger.exe local
EOF
